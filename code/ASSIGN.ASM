.MODEL SMALL
.386
.STACK 100
.DATA
	
	MSG1 DB "TYPES OF PORK$"
	ASTERISK DB "*************$"
	MSG2 DB "Enter a number (1 to 6): $"
	ERROR DB "Invalid! Please enter again: $"
	MSG3 DB "Enter the weight (1kg,2kg or 5kg): $"
	MSG4 DB "Enter the quantity: $"
	NL DB 0AH,0DH,"$"

	BELLY DB "1. PORK BESCDCSDLLY$"
	LOIN DB "2. PORK LOIN$"
	RIB DB "3. PORK RIB$"
	SHOULDER DB "4. PORK SHOULDER$"
	HAM DB "5. HAM$"
	QUIT DB "6. QUIT$"
	
	QUANTITY DB 100 DUP("$")
	WEIGHT DB ?
	CHOICE DB ?
	
	


.CODE
MAIN PROC

	MOV AX,@DATA
	MOV DS,AX
	MOV SI,OFFSET QUANTITY

	;-----DISPLAY THE MENU
	MOV AH,09H
	LEA DX,MSG1
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,ASTERISK
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,BELLY
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,LOIN
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,RIB
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,SHOULDER
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,HAM
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	;-----DISPLAY THE INPUT MESSAGE
	MOV AH,09H	
	LEA DX,MSG2
	INT 21H
		
	;-----ALLOW USER TO SELECT THE TYPE OF MEAT
	SELECTION:
		MOV AH,01H
		INT 21H
		MOV CHOICE,AL
		
		CMP AL,"6"
		JB VALID

		MOV AH,09H
		LEA DX,NL
		INT 21H

		MOV AH,09H
		LEA DX,ERROR
		INT 21H


	JMP SELECTION

	;-----ALLOW USER TO ENTER THE WEIGHT OF MEAT
	VALID:
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		MOV AH,09H
		LEA DX,MSG3		;ENTER THE WEIGHT (1,3,5KG) :
		INT 21H

		MOV AH,01H
		INT 21H
		SUB AL,30H
		MOV WEIGHT,AL

		CMP AL,3		;COMPARE THE INPUT WITH NUMBER 3
		JE CORRECT 

		MOV AH,09H
		LEA DX,NL
		INT 21H

		MOV AH,09H
		LEA DX,ERROR
		INT 21H

	JMP VALID


	;-----ALLOW USER TO ENTER THE QUANTITY
	CORRECT:
		MOV AH,09H
		LEA DX,NL
		INT 21H

		MOV AH,09H
		LEA DX,MSG4
		INT 21H
		
		LOOP1:
			MOV AH,01H
			INT 21H

			CMP AL,0DH
			JE PROGRAM

			MOV [SI],AL
			INC SI
		JMP LOOP1

		PROGRAM:
			MOV DX,OFFSET QUANTITY
			MOV AH,09H
			INT 21H
					

	MOV AX,4C00H
	INT 21H

MAIN ENDP
END MAIN