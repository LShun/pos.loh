DISPWORD PROC
	MOV DX, 0
	MOV CX, 5
	MOV OPERAND, 10000
	DIVDISPDW:
		; DIVIDE
		MOV AX, BX
		DIV OPERAND
		MOV OPERATOR, DX

		CMP AX, 0
		JZ CONTDISPWORD
		
		MOV DX, AX
		MOV AH, 02H
		ADD DL, "0"
		INT 21H
		
		CONTDISPWORD:
    		MOV AX, OPERAND
    		MOV DX, 0
    		DIV TENDW
    		MOV OPERAND, AX
    		MOV AX, OPERATOR
    		MOV BX, AX  

		LOOP DIVDISPDW
		
		RET
DISPWORD ENDP

; DISPLAY MAIN-MENU FUNCTION
DISPLAY_MENU PROC
	; DISPLAY MENU
	MOV	DX, OFFSET STRMAINMENU
	MOV	AH, 9
	INT	21H
	; ASK FOR CHOICE
	MOV	AH, 01H ; ASK FOR INPUT
	INT	21H     ; STORE IN AL
	MOV USERMENUCHOICE, AL
	RET
DISPLAY_MENU ENDP

; DISPLAY NEXT-LINE FUNCTION
NEXT_LINE PROC
	MOV 	AH, 02H
	MOV		DL, 0DH		; CR
	INT		21H
	MOV		DL, 0AH		; LF
	INT 	21H
	RET
NEXT_LINE ENDP

; DIVIDES AND DISPLAYS CONTENT IN AX REGISTER IN 5-DIGITS.
DISPAX PROC
	MOV DX, 0
	MOV CX, 5
	MOV OPERAND, 10000
	MOV DX, 0
	DIVDISP:
		; DIVIDE
		
		DIV OPERAND
		MOV OPERATOR, DX
		
		MOV DX, AX
		MOV AH, 02H
		ADD DL, "0"
		INT 21H
		
		MOV AX, OPERAND
		MOV DX, 0
		DIV TENDW
		MOV OPERAND, AX
		MOV AX, OPERATOR  

		LOOP DIVDISP
		RET
DISPAX ENDP

