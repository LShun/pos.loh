	CALL NEXT_LINE
	
	MOV AH,09H
	LEA DX,STR1
	INT 21H

	MOV DH,0
	MOV DL,MTYPE
	MOV SI,DX
	MOV AX,WEIGHT1
	MOV BH,0
	MOV BL,PRICEPKG[SI]
	MUL BX
	MOV TPRICE,AX
                                       
	MOV BX,AX
	
	CALL DISPWORD

	MOV AH,09H
	LEA DX,CENTS
	INT 21H

	CALL NEXT_LINE

	MOV AH,09H		;CONTINUE FOR PAYMENT?
	LEA DX,STR2
	INT 21H	

	MOV AH,01H
	INT 21H		;INPUT CHOICE
	MOV CONFIRMATION,AL
	
	CALL NEXT_LINE
	
	CMP CONFIRMATION,"Y"
	JE NEXT

	RET	

	NEXT: 
	
		CALL NEXT_LINE

		MOV AH,09H
		LEA DX,STR3
		INT 21H

		MOV AH,0AH
		LEA DX,INPUT_AMNT		
		INT 21H

		CALL NEXT_LINE

