.MODEL SMALL
.STACK 100
.DATA
	TITSTR DB "   CALCULATE PRICE OF MEAT$"
	STR DB "Enter weight of meat(KG): $"
	PATTERN DB 30 DUP("="),"$"
	STR1 DB "Enter quantity: $" 
	STR2 DB "Price of meat bought is RM$"
	STR3 DB "Do you wish to continue? (Y=yes,N=no)$"
	NL DB 13,10,"$"
	PRICEPKG DB 15
	TEN DB 10
	HUNDRED DB 100
	WEIGHT DB ?
	MPRICE DW ?
	QTY DB ?
	CHOICE DB ?
	
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

AGAIN:	
	MOV AH,09H		;PRINT PATTERN
	LEA DX,PATTERN
	INT 21H

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H
	
	MOV AH,09H		;PRINT TITLE
	LEA DX,TITSTR
	INT 21H

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H
	
	MOV AH,09H		;PRINT PATTERN
	LEA DX,PATTERN
	INT 21H

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT INPUT TITLE
	LEA DX,STR
	INT 21H
	
	MOV AH,01H		;INPUT WEIGHT
	INT 21H
	SUB AL,30H
	MOV WEIGHT,AL

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT INPUT QTY
	LEA DX,STR1
	INT 21H

	MOV AH,01H		;INPUT QTY
	INT 21H
	SUB AL,30H
	MOV QTY,AL

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT TOTAL PRICE
	LEA DX,STR2
	INT 21H

	MOV AL,WEIGHT
	MUL PRICEPKG		;CAL PRICE
	MUL QTY
	MOV MPRICE,AX
	
	CMP MPRICE,100		;CMP PRICE TO CHECK 3 DIGITS 
	JGE LARGNUM
		
	MOV AH,0H		;DIV TEN
	MOV AX,MPRICE		
	DIV TEN
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H

	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H	

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H			
	
	JMP CONT

	LARGNUM: 
		MOV AH,0H		;DIV HUND
		MOV AX,MPRICE		
		DIV HUNDRED
		MOV BX,AX

		MOV AH,02H		
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		MOV AH,0H		;DIV TEN
		MOV AL,BH		
		DIV TEN
		MOV BX,AX
	
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H

		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H	

		MOV AH,09H		;NEW LINE
		LEA DX,NL
		INT 21H
	
	CONT:	
		MOV AH,09H		;NEW LINE
		LEA DX,NL
		INT 21H	
	
		MOV AH,09H		;CONTINUE?
		LEA DX,STR3
		INT 21H

		MOV AH,01H		;INPUT CHOICE
		INT 21H
		MOV CHOICE,AL

		MOV AH,09H		;NEW LINE
		LEA DX,NL
		INT 21H

		MOV AH,09H		;NEW LINE
		LEA DX,NL
		INT 21H

		CMP INPUT,"N"
		
		JE DONE
		JMP AGAIN


	DONE:
		MOV AX,4C00H
		INT 21H

MAIN ENDP
END MAIN