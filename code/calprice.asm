.MODEL SMALL
.STACK 100
.DATA
	TITSTR DB "CALCULATE PRICE OF MEAT$"
	STR DB "Enter weight of meat: $"
	STR2 DB "Enter quantity: $" 
	STR1 DB "Price of meat bought is RM$"
	NL DB 13,10,"$"
	PRICEPKG DB 15
	TEN DB 10
	WEIGHT DB ?
	MPRICE DB ?
	QTY DB ?
	
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H		;PRINT TITLE
	LEA DX,TITSTR
	INT 21H

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT INPUT TITLE
	LEA DX,STR
	INT 21H
	
	MOV AH,01H		;INPUT WEIGHT
	INT 21H
	SUB AL,30H
	MOV WEIGHT,AL

	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT INPUT QTY
	LEA DX,STR2
	INT 21H

	MOV AH,01H		;INPUT QTY
	INT 21H
	SUB AL,30H
	MOV QTY,AL

	MOV AL,WEIGHT
	MUL PRICEPKG		;CAL PRICE
	MUL QTY
	MOV MPRICE,AL
	
	MOV AH,0H
	MOV AL,MPRICE
	DIV TEN
	MOV BX,AX
	
	MOV AH,09H		;NEW LINE
	LEA DX,NL
	INT 21H

	MOV AH,09H		;PRINT PRICE
	LEA DX,STR1
	INT 21H

	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H

	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H	

	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN